package com.niyati.designpattern.structural.composite;

/**
 * A Composite Design Pattern is a structural design pattern.
 * <p>
 * Composite design pattern looks like a tree structure.
 * It consists of leaf and composite.Composite Objects hold multiple components and leaf objects hold individual objects.
 * <p>
 * For example,taking account class as component and deposit and saving account as leaf.
 * Here composite account will handle all the operations.
 * <p>
 * Advantages
 * It defines class hierarchies that contain primitive and complex objects.
 * It makes easier to add new kinds of components.
 * <p>
 * Disadvantages
 * Once tree structure is defined,the composite design makes the tree overly general.
 */