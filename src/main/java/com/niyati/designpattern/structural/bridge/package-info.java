package com.niyati.designpattern.structural.bridge;
/*
Bridge design pattern is a structural design pattern .
It is used when we have hierarchies in both interfaces as well as implementations.

It separates the abstraction from its implementation so that 2 can vary independently.

For example, generally we have a remote(interface) which works differently for different Tv(class) like Sony and Philips Tv , but we can have different Remote as well , i.e old remote and new remote.
It is bridging the hierarchy of classes to the interface.



*/