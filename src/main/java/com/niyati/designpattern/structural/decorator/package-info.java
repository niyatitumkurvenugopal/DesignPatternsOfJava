package com.niyati.designpattern.structural.decorator;

/*
A Decorator Design Pattern is a structural design pattern.

Decorator design pattern is used to modify the functionality of an object at runtime.
It should not change individual object functionality.

For example, Adding different functionalities in Car.
Taking a car as an interface and BasicCar and CarDecorator implements the Car interface.
Whereas LuxuryCar and SportCar are extensions of CarDecorator.

*/